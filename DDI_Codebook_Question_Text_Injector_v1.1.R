# New things added to  v1.1: AI Hint Classification into preQTxt / postQTxt[literal questions with pre/post question texts]
# ---------------------------------------------------------------------------
#- Reads the hint and uses free Ollama locally installed model called 'gemma3' to classify whether the hint is pre/post literal question text 
#
# REQUIREMENT(S): 
